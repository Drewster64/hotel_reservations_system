class Cliente:
    def __init__(self, id, nombre, direccion, telefono, email, fecha_registro):
        self.id = id  # Identificador único del cliente
        self.nombre = nombre  # Nombre del cliente
        self.direccion = direccion  # Dirección del cliente
        self.telefono = telefono  # Teléfono del cliente
        self.email = email  # Correo electrónico del cliente
        self.fecha_registro = fecha_registro  # Fecha de registro del cliente

